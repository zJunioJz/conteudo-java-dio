package sintaxe.estrutura_sequencial;

public class Processamento {

    public static void main(String[] args) {

        int x = 7;
        double y = 2 * x;

        double b, B, h, area;

        b = 6.0;
        B = 8.0;
        h = 5.0;

        area = ((b + B) * h) / 2;

        System.out.println(x);
        System.out.println(y);

        System.out.println(area);
    }

}
